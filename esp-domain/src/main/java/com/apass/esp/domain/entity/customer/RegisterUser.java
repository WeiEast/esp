package com.apass.esp.domain.entity.customer;

public class RegisterUser {
	
	private String androidNum;
	
	private String iosNum;
	
	private String totalNum;

	public String getAndroidNum() {
		return androidNum;
	}

	public void setAndroidNum(String androidNum) {
		this.androidNum = androidNum;
	}

	public String getIosNum() {
		return iosNum;
	}

	public void setIosNum(String iosNum) {
		this.iosNum = iosNum;
	}

	public String getTotalNum() {
		return totalNum;
	}

	public void setTotalNum(String totalNum) {
		this.totalNum = totalNum;
	}
	
}
